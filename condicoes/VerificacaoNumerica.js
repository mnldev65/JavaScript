// Número Positivo, Negativo ou Zero
// Objetivo

// Receber um número e verificar:

// - Positivo
// - Negativo
// - Zero

const num = -1;
if (num >= 1) {
    console.log(`O número ${num} é positivo`);
} else if (num == 0) {
    console.log(`O número ${num} é igual a zero`);
} else {
    console.log(`O número ${num} é negativo`);
}